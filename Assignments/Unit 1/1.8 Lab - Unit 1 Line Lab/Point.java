public class Point {
    private int x;
    private int y;

    // constructors
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    // getters
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    // setters
    public void setX(int input) {
        x = input;
    }

    public void setY(int input) {
        y = input;
    }

}
