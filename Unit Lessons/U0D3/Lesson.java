public class Lesson {

    public static void main(String[] args) {
        // variables
        int number = 5; // assignment operator

        // single = assigns a value
        // different from ==, which compares a value

        // different assignment operators
        number += 6; // same as number = number + 6
        number -= 3; // number = number - 3
        number *= 10; // number = number * 10
        number /= 2; // number = number / 2

        // incrememnt and decrement operators
        number += 1;
        number++;

        number -= 1; 
        number--;

        // boolean operators, evaluate to true or false
        // use them in if statements

        // You cannot use this for objects
        
        //number == 5; // equals
        //number != 10 // not equals
        //number <= 15 // less than or equals
        //number >= 20 // greater than or equals
    }
}